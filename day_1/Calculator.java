public class Calculator {
    //method
    public int sum(int num1, int num2) {
        int res = num1 + num2;
        return res;
    }
    
    public  int minus(int num1, int num2) {
        int res = num1 - num2;
        return res;
    }
    
    public  int multiply(int num1, int num2) {
        int res = num1 * num2;
        return res;
    }
    
    public  int divide(int num1, int num2) {
        int res = num1 / num2;
        return res;
    }
    
}
